CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

CREATE TABLE IF NOT EXISTS players (
    username VARCHAR(255),
    uuid UUID DEFAULT uuid_generate_v4(),
    joined_date TIMESTAMP WITH TIME ZONE,
    op BOOLEAN,
    email VARCHAR(255),
    first_name VARCHAR(255),
    last_name VARCHAR(255)
);

CREATE TABLE IF NOT EXISTS characters (
    id SERIAL PRIMARY KEY,
    player_uuid UUID REFERENCES players(uuid),
    name VARCHAR(255),
    age INTEGER,
    race VARCHAR(100),
    faith VARCHAR(100),
    hair VARCHAR(50),
    eyes VARCHAR(50),
    skin VARCHAR(50),
    height VARCHAR(32),
    weight INTEGER,
    armor_class INTEGER,
    initiative INTEGER,
    walking_speed INTEGER,
    base_hit_points INTEGER,
    urrent_xp INTEGER DEFAULT 0,
    lifestyle_id INTEGER,
    strength INTEGER,
    dexterity INTEGER,
    constitution INTEGER,
    intelligence INTEGER,
    wisdom INTEGER,
    charisma INTEGER,
    acrobatics INTEGER,
    animal_handling INTEGER,
    arcana INTEGER,
    athletics INTEGER,
    deception INTEGER,
    history INTEGER,
    insight INTEGER,
    intimidation INTEGER,
    investivation INTEGER,
    medicine INTEGER,
    nature INTEGER,
    perception INTEGER,
    performance INTEGER,
    persuasion INTEGER,
    religion INTEGER,
    sleight_of_hand INTEGER,
    stealth INTEGER,
    survival INTEGER,
    inspiration BOOLEAN DEFAULT FALSE,
    bonus_hit_points INTEGER DEFAULT 0,
    temporary_hit_points INTEGER DEFAULT 0,
    platinum_amount DECIMAL(10, 2),
    gold_amount DECIMAL(10, 2),
    electrum_amount DECIMAL(10, 2),
    silver_amount DECIMAL(10, 2),
    copper_amount DECIMAL(10, 2)
);
